// 2. Function to Open Tips
function openTips(imageSrc) {
    const data = tipsDatabase[imageSrc];
    const viewer = document.getElementById('tips-viewer');
    
    if (data) {
        document.getElementById('view-title').innerText = data.title;
        document.getElementById('view-image').src = imageSrc;
        document.getElementById('view-text').innerHTML = data.content;
        
        viewer.classList.remove('loader-hidden');
        window.scrollTo({ top: 0, behavior: 'smooth' });
    }
}



